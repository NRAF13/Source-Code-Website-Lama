	<h2 class="center-normal">PERATURAN ROLEPLAY</h2>

	<br>

	<p class="req">1.1 ROLEPLAY<br>

		Adalah permainan peran dimana kalian akan mengendalikan karakter layalknya di dunia nyata, apapun yang karakter kalian lakukan itu semua tergantung kalian sebagai pemeran dan segala hal tentang karakter yang kalian kendalikan tidak memiliki sangkut pautnya dengan dunia nyata</p>

	<br>

	<p class="req">1.2 IN CHARACTER<br>

	Adalah segala hal yang terjadi yang berhubungan dengan karakter kalian di dalam game, IC wajib bersih dari segala hal yang berbau dunia nyata. serta wajib juga senyata dan semirip mungkin apapun yang kita lakukan di IC seperti di dunia nyata.</p>

	<br>

	<p class="req">1.3 OUT OF CHARACTER<br>

	atau OOC adalah segala hal yang berhubungan dengan dunia nyata kita, OOC tidak ada hubungan serta sangkut pautnya dengan IC. Jadi apapun yang terjadi di IC tidak berlaku di OOC begitu pun sebaliknya.</p>

	<br>

	<p class="req">1.4 MIXXING<br>

	Mencampurkan informasi IC dan OOC untuk keuntungan masing masing pihak dalam ber-RP, seperti kalian melihat nametag diatas kepala lalu menyebut nama pemain lain tanpa berkenalan.</p>

	<br>

	<p class="req">1.5 POWERGAMING<br>

	Adalah memaksakan kehendak diri sendiri dalam ber-RP, dan melakukan suatu hal yang tidak bisa terjadi di dunia nyata.</p>

	<br>

	<p class="req">1.6 METAGAMING<br>

	Adalah melakukan sesuatu yang tidak sesuai dengan kodratnya tanpa ada alasan ataupun RP yang jelas</p>

	<br>

	<p class="req">1.7 Non-RP NAME<br>

		Adalah menggunakan nama yang tidak masuk akal seperti <b>Aku_Tampan,Raja_Barong.</b> hal itu sangat dilarang.</p>

	<br>

	<p class="req">1.8 Non-RP FEAR<br>

	Adalah kalian tidak memiliki perasaan apapun saat ber-RP. Seperti tidak takut saat ditodongkan senjata, tidak lelah saat berlari atau berenang atau bersepeda dalam waktu yang lama.</p>

	<br>

	<p class="req">1.9 GREENZONE<br>

		Adalah zona terlarang untuk melakukan tindakan kejahatan apapun. Seperti di Kantor Presiden, Kantor Polisi DLL.</p>

	<br>

	<p class="req">1.10 Non-RP DRIVING<br>

	Adalah berkendara dengan RP yang tidak benar</p>

